package Buoi_5.src.Bai_4;

public class Sum<X> {
    X a, b;

    public Sum() {
    }

    public Sum(X a, X b) {
        this.a = a;
        this.b = b;
    }

    public X getA() {
        return a;
    }

    public void setA(X a) {
        this.a = a;
    }

    public X getB() {
        return b;
    }

    public void setB(X b) {
        this.b = b;
    }
    
}
